    public void popTable(String Eid){
        
        double Tot=0;
        
        double Tup=0,Tqty=0;
      try {
            //count
            
            
            
            
            //table
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            
            
            ArrayList<Event_Service> array = serSrv.getEventServiceList(Eid);
            
            Object rawdata[] = new Object[7];
            
            for(int i=0; i<array.size();i++){
                
                rawdata[0]=array.get(i).getServiceID();
                rawdata[1]=array.get(i).getSname();
                rawdata[2]=array.get(i).getInfo();
                rawdata[3]=array.get(i).getSupplierName();
                rawdata[4]=array.get(i).getQty();
                rawdata[5]=array.get(i).getUnitPrice();
                rawdata[6]=array.get(i).getSupplierEmail();
                
                Tup=Double.valueOf(array.get(i).getUnitPrice());
                Tqty=Double.valueOf(array.get(i).getQty());
                
                Tot = Tot + (Tup*Tqty);
                array.get(i).toString();
                
                
                model.addRow(rawdata);
                
                
                
            }
            
            
            // Variables declaration - do not modify
        } catch (SQLException ex) {
            Logger.getLogger(CustomerHome.class.getName()).log(Level.SEVERE, null, ex);
            
             JPanel pane5 = new JPanel();
                    JOptionPane.showMessageDialog(pane5, "Error Loading the Event Contents", "Error", JOptionPane.ERROR_MESSAGE);
                    
             dispose();
             new CustomerLogin().setVisible(true);
        }

        subTot.setText(String.valueOf(Tot));
    }

