    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        try {
            // TODO add your handling code here:
            
            
            this.evt.setDate(date.getText());
            this.evt.setDuration(duration.getText());
            this.evt.setName(name.getText());
            this.evt.setStime(stime.getText());
            this.evt.setType(type.getText());
            
            
            if(srv.updateEvent(this.evt)){
                
                JPanel pane3 = new JPanel();
                JOptionPane.showMessageDialog(pane3, "Successfull Updated.", "Successfull", JOptionPane.INFORMATION_MESSAGE);
                
                new CustomerHome(csrv.getCustomer(Cemail)).setVisible(true);
            }
        } catch (SQLException ex) {
            Logger.getLogger(CustomerViewEvent.class.getName()).log(Level.SEVERE, null, ex);
            
            JPanel pane3 = new JPanel();
                JOptionPane.showMessageDialog(pane3, "Error Occured.", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            Logger.getLogger(CustomerViewEvent.class.getName()).log(Level.SEVERE, null, ex);
                  
            JPanel pane3 = new JPanel();
                JOptionPane.showMessageDialog(pane3, "Error Occured.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

